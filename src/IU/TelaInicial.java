      private void jMenuItemCadClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemCadClienteActionPerformed
            // TODO add your handling code here:
            TelaCadastroCliente telaCadCliente = new TelaCadastroCliente(this, true);
            telaCadCliente.setLocationRelativeTo(this);
            telaCadCliente.setVisible(true);
            control.cadastrarCliente(Integer.parseInt(telaCadCliente.getCod()), telaCadCliente.getNome(), telaCadCliente.getCpf(), telaCadCliente.getRg(),  telaCadCliente.getDiaNasc(), telaCadCliente.getMesNasc(), telaCadCliente.getAnoNasc(), telaCadCliente.getEndereco(), telaCadCliente.getCep(), telaCadCliente.getTelefone(), telaCadCliente.getEmail() , telaCadCliente.getDiaCad(), telaCadCliente.getMesCad(), telaCadCliente.getAnoCad());
            try {
                  control.atualizarArquivos();
            } catch (IOException ex) {
                  Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                  Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
      }//GEN-LAST:event_jMenuItemCadClienteActionPerformed

