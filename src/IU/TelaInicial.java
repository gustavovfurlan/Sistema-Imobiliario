      private void jMenuItemAddVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemAddVendaActionPerformed
            // TODO add your handling code here:
            TelaVenda venda = new TelaVenda(this, true);
            venda.setLocationRelativeTo(this);
            venda.setVisible(true);
            if(control.vender(Integer.parseInt(venda.getCodVenda()), Integer.parseInt(venda.getCodImovel()), Integer.parseInt(venda.getCodCliente()), Integer.parseInt(venda.getCodCorretor()), venda.getNome(), venda.getBandeira(), venda.getNumero())){
                  JOptionPane.showMessageDialog(rootPane, "DEu");
            }
            try {
                  control.atualizarArquivos();
            } catch (IOException ex) {
                  Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                  Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
      }//GEN-LAST:event_jMenuItemAddVendaActionPerformed

